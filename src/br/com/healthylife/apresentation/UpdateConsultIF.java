    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        try {
        ControllerConsultation cc = new ControllerConsultation();

        String id = jTName.getText();
        String folder = "Consultation";

        File file = new File(folder, id + ".data");

        if (file.exists()) {
            cc.search(id + ".data");
            
            enableFields();
            
        } else {
            throw new NullPointerException("Consulta n√£o encontrada!");
        }
        }catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButtonSearchActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
       
        try {
            String name = jTName.getText();
            String hospital = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            Date date = jDateChooser1.getDate();
            
            ControllerConsultation cc = new ControllerConsultation();
            
            cc.updateConsult(name, hospital, date);
            
            JOptionPane.showConfirmDialog(this, "Remarcada com sucesso!");
            this.dispose();
            
        } catch (NullPointerException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed


